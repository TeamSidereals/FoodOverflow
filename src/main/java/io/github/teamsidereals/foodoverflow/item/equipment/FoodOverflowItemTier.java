package io.github.teamsidereals.foodoverflow.item.equipment;

import io.github.teamsidereals.foodoverflow.registry.FoodOverflowItemsRegister;
import net.minecraft.item.IItemTier;
import net.minecraft.item.Items;
import net.minecraft.item.crafting.Ingredient;
import net.minecraft.tags.ItemTags;
import net.minecraft.util.LazyValue;

import java.util.function.Supplier;

public enum FoodOverflowItemTier implements IItemTier {
    //(harvestLevel, maxUse, efficiency, attackDamage, enchantability, repairMaterial)
    CARAMEL(1, 110, 4.5F, 1.5F, 8, ()
            -> Ingredient.of(FoodOverflowItemsRegister.CARAMEL_BAR.get()));

    private final int harvestLevel;
    private final int maxUses;
    private final float efficiency;
    private final float attackDamage;
    private final int enchantability;
    private final LazyValue<Ingredient> repairMaterial;

    FoodOverflowItemTier(int harvestLevel, int maxUses, float efficiency,
                float attackDamage, int enchantability, Supplier<Ingredient> repairMaterial) {
        this.harvestLevel = harvestLevel;
        this.maxUses = maxUses;
        this.efficiency = efficiency;
        this.attackDamage = attackDamage;
        this.enchantability = enchantability;
        this.repairMaterial = new LazyValue<>(repairMaterial);
    }

    @Override
    public int getUses() {
        return maxUses;
    }

    @Override
    public float getSpeed() {
        return efficiency;
    }

    @Override
    public float getAttackDamageBonus() {
        return attackDamage;
    }

    @Override
    public int getLevel() {
        return harvestLevel;
    }

    @Override
    public int getEnchantmentValue() {
        return enchantability;
    }

    @Override
    public Ingredient getRepairIngredient() {
        return repairMaterial.get();
    }
}
